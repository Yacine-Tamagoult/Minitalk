#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	long unsigned int		i;
	unsigned char			*ptr;

	i = 0;
	ptr = (unsigned char *)s;
	while (i < n)
	{
		ptr[i] = 0;
		i++;
	}
}
